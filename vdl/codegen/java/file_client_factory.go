package java

import (
	"bytes"
	"log"
	"path"

	"veyron2/vdl/compile"
)

const clientFactoryTmpl = `// This file was auto-generated by the veyron vdl tool.
// Source(s):  {{ .Sources }}
package {{ .PackagePath }};

/* Factory for binding to {{ .ServiceName }} interfaces. */
public final class {{ .ServiceName }}Factory {
    public static {{ .ServiceName }} bind(final java.lang.String name) throws com.veyron2.ipc.VeyronException {
        return bind(name, null);
    }
    public static {{ .ServiceName }} bind(final java.lang.String name, final com.veyron2.Options veyronOpts) throws com.veyron2.ipc.VeyronException {
        com.veyron2.ipc.Client client = null;
        if (veyronOpts != null && veyronOpts.get(com.veyron2.OptionDefs.CLIENT) != null) {
            client = veyronOpts.get(com.veyron2.OptionDefs.CLIENT, com.veyron2.ipc.Client.class);
        } else if (veyronOpts != null && veyronOpts.get(com.veyron2.OptionDefs.RUNTIME) != null) {
            client = veyronOpts.get(com.veyron2.OptionDefs.RUNTIME, com.veyron2.Runtime.class).getClient();
        } else {
            client = com.veyron2.RuntimeFactory.defaultRuntime().getClient();
        }
        return new {{ .StubName }}(client, name);
    }
}
`

// genJavaClientFactoryFile generates the Java file containing client bindings for
// all interfaces in the provided package.
func genJavaClientFactoryFile(iface *compile.Interface, env *compile.Env) JavaFileInfo {
	data := struct {
		Sources     string
		ServiceName string
		PackagePath string
		StubName    string
	}{
		Sources:     iface.File.BaseName,
		ServiceName: iface.Name,
		PackagePath: javaPath(javaGenPkgPath(iface.File.Package.Path)),
		StubName:    javaPath(javaGenPkgPath(path.Join(iface.File.Package.Path, javaGenImplDir, iface.Name+"Stub"))),
	}
	var buf bytes.Buffer
	err := parseTmpl("client factory", clientFactoryTmpl).Execute(&buf, data)
	if err != nil {
		log.Fatalf("vdl: couldn't execute client template: %v", err)
	}
	return JavaFileInfo{
		Name: iface.Name + "Factory.java",
		Data: buf.Bytes(),
	}
}
